public class SoalArray1 {
    public static void main(String[] args) {
        String [] namakota = {"Jakarta","Bandung","Surabaya","Medan","Semarang","Palembang"};

        for (int i = 0; i < namakota.length; i++) {
            System.out.println("Kota ke-" + (i + 1) + " : " + namakota[i]);
        }
        
    }
}
